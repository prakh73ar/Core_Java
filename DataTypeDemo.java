package learning.oops;

class DataTypeDemo {
    public static void main(String[] args) {
        int age = 20;
        String name = "Scott";
        float salary = 80000.99f;
        System.out.println("Hello " + name + " Your age is " + age + " and your salary is " + salary);
        long phone = 7307755213L;
        System.out.println(name + " Your phone no. is " + phone);
        long id = 1133;
        System.out.println(name + " Your id. is " + id);

    }

}